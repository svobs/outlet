from model.uid import UID
from util.ensure import ensure_int


class Device:
    """
    ▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼
    CLASS Device
    ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼
    """
    def __init__(self, device_uid: UID, device_id: str, tree_type: int, friendly_name: str, sync_ts: int):
        self.uid: UID = device_uid
        self.device_id: str = device_id
        self.tree_type: int = tree_type
        self.friendly_name: str = friendly_name
        self.sync_ts = ensure_int(sync_ts)

    def to_tuple(self):
        return self.uid, self.device_id, self.tree_type, self.friendly_name, self.sync_ts

    def __repr__(self):
        return f'Device(uid="{self.uid}" device_id={self.device_id} tree_type={self.tree_type} friendly_name={self.friendly_name} ' \
               f'sync_ts={self.sync_ts})'
