from constants import TreeType
from model.uid import UID
from util.ensure import ensure_tree_type


class Device:
    """
    ▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼▼
    CLASS Device

    Represents the source of a particular tree in the cache. Some examples include a local disk, or a user's Google Drive account.
    ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼ ▼
    """
    def __init__(self, device_uid: UID, long_device_id: str, tree_type: TreeType, friendly_name: str, ):
        self.uid: UID = device_uid
        self.long_device_id: str = long_device_id
        """Longer unique identifier for the device; no standard format"""
        self.tree_type: TreeType = ensure_tree_type(tree_type)
        self.friendly_name: str = friendly_name

    def to_tuple(self):
        return self.uid, self.long_device_id, self.tree_type, self.friendly_name

    def __repr__(self):
        return f'Device(uid="{self.uid}" long_device_id={self.long_device_id} tree_type={self.tree_type} friendly_name={self.friendly_name})'
