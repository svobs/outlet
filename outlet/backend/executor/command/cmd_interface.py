    def __repr__(self):
        return f'{self.__class__.__name__} status={self.get_status()} total_work={self.get_total_work()}' \
               f' overwrite={self.overwrite} src={self.op.src_node.node_identifier} dst={self.op.dst_node.node_identifier})'


